function UpdatePago() {
    var formulario = document.querySelector(".UpdatePago");
    if (formulario.style.display === "none") {
      formulario.style.display = "block";
    } else {
      formulario.style.display = "none";
    }
  }

  function AgregarPago() {
    var formulario = document.querySelector(".AgregarPago");
    if (formulario.style.display === "none") {
      formulario.style.display = "block";
    } else {
      formulario.style.display = "none";
    }
  }